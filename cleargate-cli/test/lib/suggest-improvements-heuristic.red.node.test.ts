// ── Fixture helpers ───────────────────────────────────────────────────────────

const tmpDirs: string[] = [];

function makeTmpDir(): string {
  const d = fs.mkdtempSync(path.join(os.tmpdir(), 'cg-suggest-heuristic-'));
  tmpDirs.push(d);
  return d;
}

function cleanupTmpDirs(): void {
  for (const d of tmpDirs.splice(0)) {
    fs.rmSync(d, { recursive: true, force: true });
  }
}

interface LedgerEntry {
  ts: string;
  sprint_id: string;
  story_id: string;
  work_item_id: string;
  agent_type: string;
  session_id: string;
  transcript: string;
  delta: { input: number; output: number; cache_creation: number; cache_read: number };
  model: string;
  turns: number;
}

function makeLedgerEntry(
  overrides: Partial<LedgerEntry> & Pick<LedgerEntry, 'work_item_id' | 'agent_type' | 'session_id' | 'sprint_id'>,
): LedgerEntry {
  return {
    ts: '2026-05-04T10:00:00Z',
    story_id: '',
    transcript: '/fake/transcript.jsonl',
    delta: { input: 100, output: 1000, cache_creation: 0, cache_read: 0 },
    model: 'claude-sonnet-4-6',
    turns: 10,
    ...overrides,
  };
}

/**
 * Build a sprint directory under a given sprints-root:
 *   <sprintsRoot>/<sprintId>/
 *     token-ledger.jsonl   (created from ledgerEntries)
 *     improvement-suggestions.md  (created if suggestionsContent provided)
 */
function buildSprintDir(
  sprintsRoot: string,
  sprintId: string,
  opts: {
    ledgerEntries?: LedgerEntry[];
    suggestionsContent?: string;
  } = {},
): string {
  const sprintDir = path.join(sprintsRoot, sprintId);
  fs.mkdirSync(sprintDir, { recursive: true });

  if (opts.ledgerEntries && opts.ledgerEntries.length > 0) {
    const ledgerContent = opts.ledgerEntries
      .map((e) => JSON.stringify(e))
      .join('\n') + '\n';
    fs.writeFileSync(path.join(sprintDir, 'token-ledger.jsonl'), ledgerContent, 'utf8');
  }

  if (opts.suggestionsContent !== undefined) {
    fs.writeFileSync(
      path.join(sprintDir, 'improvement-suggestions.md'),
      opts.suggestionsContent,
      'utf8',
    );
  }

  return sprintDir;
}

/**
 * Run suggest_improvements.mjs with --skill-candidates mode.
 * Returns the exit code and the resulting improvement-suggestions.md content.
 */
function runSkillCandidates(opts: {
  sprintId: string;
  sprintDir: string;
  sprintRunsDir?: string;
  flashcardPath?: string;
}): { exitCode: number; suggestionsContent: string } {
  const env: Record<string, string> = {
    ...(process.env as Record<string, string>),
    CLEARGATE_SPRINT_DIR: opts.sprintDir,
  };

  if (opts.sprintRunsDir) {
    env['CLEARGATE_SPRINT_RUNS_DIR'] = opts.sprintRunsDir;
  }

  if (opts.flashcardPath) {
    env['CLEARGATE_FLASHCARD_PATH'] = opts.flashcardPath;
  }

  // Scrub NODE_TEST_CONTEXT per FLASHCARD 2026-05-04 #node-test #child-process
  delete env['NODE_TEST_CONTEXT'];

  const result = spawnSync('node', [SCRIPT_PATH, opts.sprintId, '--skill-candidates'], {
    encoding: 'utf8',
    timeout: 30_000,
    env,
  });

  const suggestionsFile = path.join(opts.sprintDir, 'improvement-suggestions.md');
  const suggestionsContent = fs.existsSync(suggestionsFile)
    ? fs.readFileSync(suggestionsFile, 'utf8')
    : '';

  return {
    exitCode: result.status ?? -1,
    suggestionsContent,
  };
}

