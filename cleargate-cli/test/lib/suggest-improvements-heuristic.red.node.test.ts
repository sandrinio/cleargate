// ── Scenario 1: session-shared filtered out ───────────────────────────────────
//
// Fixture: 3 ledger entries all sharing the SAME session UUID,
//          same work_item_id (CR-045) and agent_type (architect),
//          spread across 2 sprints (entries in current sprint's ledger to give ≥3 count).
//
// Expected post-fix: NOT flagged as skill candidate (session-shared filter triggers).
// Baseline behaviour: flagged (heuristic has no session-shared filter → ≥3 count → flag).
// Test assertion: candidate NOT in suggestions → FAILS on baseline.

describe('Scenario 1: session-shared entries NOT flagged as skill candidate', () => {
  it('session-shared attribution: same session_id for all 3 CR-045×architect entries → NOT flagged', () => {
    const tmpRoot = makeTmpDir();
    const SHARED_SESSION = '48aa90c9-f20f-4899-ba85-1079373f3d8e'; // real session from SPRINT-23 data
    const sprintId = 'SPRINT-TEST-S1';

    // 3 entries all in the same session, same work_item_id|agent_type
    // Note: 2 marked sprint_id=SPRINT-23, 1 marked sprint_id=SPRINT-24
    //       to simulate the real cross-sprint session-sharing pattern.
    //       All 3 end up in the current sprint's ledger file (as the tool reads it).
    const entries: LedgerEntry[] = [
      makeLedgerEntry({
        sprint_id: 'SPRINT-23',
        work_item_id: 'CR-045',
        agent_type: 'architect',
        session_id: SHARED_SESSION,
      }),
      makeLedgerEntry({
        sprint_id: 'SPRINT-23',
        work_item_id: 'CR-045',
        agent_type: 'architect',
        session_id: SHARED_SESSION,
      }),
      makeLedgerEntry({
        sprint_id: 'SPRINT-24',
        work_item_id: 'CR-045',
        agent_type: 'architect',
        session_id: SHARED_SESSION,
      }),
    ];

    const sprintDir = buildSprintDir(tmpRoot, sprintId, { ledgerEntries: entries });

    // Empty flashcard to avoid false positives from "also do" pattern
    const emptyFlashcard = path.join(tmpRoot, 'FLASHCARD.md');
    fs.writeFileSync(emptyFlashcard, '# ClearGate Flashcards\n', 'utf8');

    const { exitCode, suggestionsContent } = runSkillCandidates({
      sprintId,
      sprintDir,
      flashcardPath: emptyFlashcard,
    });

    assert.strictEqual(exitCode, 0, `script should exit 0, got: ${exitCode}`);

    // Post-fix: session-shared entries are filtered → candidate absent
    // Baseline: no filter → candidate present → this assertion FAILS (RED)
    const hasCandidate = suggestionsContent.includes('CR-045 × architect') ||
      suggestionsContent.includes('<!-- hash:6b1802 -->');

    assert.strictEqual(
      hasCandidate,
      false,
      'session-shared CR-045×architect tuple must NOT be flagged as a skill candidate\n' +
      `Suggestions file content:\n${suggestionsContent}`,
    );
  });
});

// ── Scenario 2: real pattern (cross-sprint, distinct sessions) still flagged ──
//
// Fixture: 3 ledger entries across 2 sprints, DISTINCT session UUIDs,
//          same work_item_id|agent_type (CR-REAL × developer).
//          Current sprint has 2 entries; prior sprint has 1 entry.
//          Total across both sprints = 3 (≥3 threshold met).
//
// Expected post-fix: IS flagged (cross-sprint aggregation with distinct sessions).
// Baseline behaviour: reads only current sprint's ledger → 2 entries < ≥3 → NOT flagged.
// Test assertion: candidate IS in suggestions → FAILS on baseline.

describe('Scenario 2: cross-sprint distinct-session pattern IS flagged as skill candidate', () => {
  it('CR-REAL×developer: 3 entries across 2 sprints with distinct sessions → IS flagged', () => {
    const tmpRoot = makeTmpDir();
    const SESSION_A = 'aaaaaaaa-aaaa-aaaa-aaaa-aaaaaaaaaaaa';
    const SESSION_B = 'bbbbbbbb-bbbb-bbbb-bbbb-bbbbbbbbbbbb';
    const currentSprintId = 'SPRINT-CURR';
    const priorSprintId = 'SPRINT-PRIOR';

    // Prior sprint: 1 entry (distinct session A)
    buildSprintDir(tmpRoot, priorSprintId, {
      ledgerEntries: [
        makeLedgerEntry({
          sprint_id: priorSprintId,
          work_item_id: 'CR-REAL',
          agent_type: 'developer',
          session_id: SESSION_A,
        }),
      ],
    });

    // Current sprint: 2 entries (distinct session B)
    // Note: only 2 entries here — current heuristic sees 2 < 3 → NOT flagged
    const currentSprintDir = buildSprintDir(tmpRoot, currentSprintId, {
      ledgerEntries: [
        makeLedgerEntry({
          sprint_id: currentSprintId,
          work_item_id: 'CR-REAL',
          agent_type: 'developer',
          session_id: SESSION_B,
        }),
        makeLedgerEntry({
          sprint_id: currentSprintId,
          work_item_id: 'CR-REAL',
          agent_type: 'developer',
          session_id: SESSION_B,
        }),
      ],
    });

    const emptyFlashcard = path.join(tmpRoot, 'FLASHCARD.md');
    fs.writeFileSync(emptyFlashcard, '# ClearGate Flashcards\n', 'utf8');

    const { exitCode, suggestionsContent } = runSkillCandidates({
      sprintId: currentSprintId,
      sprintDir: currentSprintDir,
      sprintRunsDir: tmpRoot,  // so the heuristic can find the prior sprint's ledger
      flashcardPath: emptyFlashcard,
    });

    assert.strictEqual(exitCode, 0, `script should exit 0, got: ${exitCode}`);

    // Post-fix: cross-sprint aggregation → total 3 → flagged
    // Baseline: only reads current sprint → 2 entries → NOT flagged → this assertion FAILS (RED)
    const hasCandidate =
      suggestionsContent.includes('CR-REAL × developer') ||
      suggestionsContent.includes('CR-REAL|developer');

    assert.strictEqual(
      hasCandidate,
      true,
      'CR-REAL×developer with 3 entries across 2 distinct sessions MUST be flagged as a skill candidate\n' +
      `Suggestions file content:\n${suggestionsContent}`,
    );
  });
});

// ── Scenario 3: cross-sprint dedup prevents re-surfacing ─────────────────────
//
// Fixture: prior sprint's improvement-suggestions.md already contains
//          <!-- hash:6b1802 --> (the CR-045×architect candidate hash).
//          Current sprint's token-ledger has 3 entries for CR-045×architect
//          with DISTINCT sessions (i.e., a "real" signal if session filter passes).
//
// Expected post-fix: candidate NOT re-surfaced (cross-sprint dedup finds prior hash).
// Baseline behaviour: dedup only checks current sprint's suggestions file →
//                     prior sprint's hash not found → candidate RE-SURFACED.
// Test assertion: candidate NOT in current suggestions → FAILS on baseline.

describe('Scenario 3: cross-sprint dedup suppresses already-seen candidate', () => {
  it('hash:6b1802 already in prior sprint suggestions → NOT re-surfaced in current sprint', () => {
    const tmpRoot = makeTmpDir();
    const SESSION_C = 'cccccccc-cccc-cccc-cccc-cccccccccccc';
    const SESSION_D = 'dddddddd-dddd-dddd-dddd-dddddddddddd';
    const SESSION_E = 'eeeeeeee-eeee-eeee-eeee-eeeeeeeeeeee';
    const currentSprintId = 'SPRINT-DEDUP';
    const priorSprintId = 'SPRINT-PRIOR-SEEN';

    // Prior sprint's improvement-suggestions.md already contains the candidate hash
    const priorSuggestionsContent = [
      `# Improvement Suggestions — ${priorSprintId}`,
      '',
      '## Skill Creation Candidates',
      '',
      '<!-- generated-by: suggest_improvements.mjs --skill-candidates -->',
      '',
      `### CAND-${priorSprintId}-S01: CR-045 × architect`,
      '<!-- hash:6b1802 -->',
      '',
      '**Pattern detected:** CR-045 × architect repeated ≥3× in token-ledger',
      '**Proposed skill:** `.claude/skills/<slug>/SKILL.md`',
      '',
      '---',
      '',
    ].join('\n');

    buildSprintDir(tmpRoot, priorSprintId, {
      suggestionsContent: priorSuggestionsContent,
    });

    // Current sprint: 3 entries with distinct sessions (would pass session filter post-fix)
    const currentSprintDir = buildSprintDir(tmpRoot, currentSprintId, {
      ledgerEntries: [
        makeLedgerEntry({
          sprint_id: currentSprintId,
          work_item_id: 'CR-045',
          agent_type: 'architect',
          session_id: SESSION_C,
        }),
        makeLedgerEntry({
          sprint_id: currentSprintId,
          work_item_id: 'CR-045',
          agent_type: 'architect',
          session_id: SESSION_D,
        }),
        makeLedgerEntry({
          sprint_id: currentSprintId,
          work_item_id: 'CR-045',
          agent_type: 'architect',
          session_id: SESSION_E,
        }),
      ],
      // Current sprint has NO existing suggestions.md yet
    });

    const emptyFlashcard = path.join(tmpRoot, 'FLASHCARD.md');
    fs.writeFileSync(emptyFlashcard, '# ClearGate Flashcards\n', 'utf8');

    const { exitCode, suggestionsContent } = runSkillCandidates({
      sprintId: currentSprintId,
      sprintDir: currentSprintDir,
      sprintRunsDir: tmpRoot,  // so the heuristic can find the prior sprint's suggestions
      flashcardPath: emptyFlashcard,
    });

    assert.strictEqual(exitCode, 0, `script should exit 0, got: ${exitCode}`);

    // Post-fix: cross-sprint dedup finds hash:6b1802 in prior sprint → suppresses
    // Baseline: only checks current sprint's suggestions → not found → re-surfaces
    //           → this assertion FAILS (RED)
    const resSurfaced =
      suggestionsContent.includes('<!-- hash:6b1802 -->') &&
      // Allow "seen_in" marker as a valid suppression form per CR-056 §0.5 Q2 default
      !suggestionsContent.includes('seen_in:');

    assert.strictEqual(
      resSurfaced,
      false,
      'CR-045×architect must NOT be re-surfaced when hash:6b1802 already exists in a prior sprint\'s suggestions\n' +
      'Acceptable outcomes: (a) absent from suggestions entirely, or (b) present with seen_in: [...] marker\n' +
      `Suggestions file content:\n${suggestionsContent}`,
    );
  });
});
